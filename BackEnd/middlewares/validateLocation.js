// Ray-casting: devuelve true si el punto está dentro del polígono
function isInsidePolygon(lat, lng, polygon) {
  let inside = false;
  const n = polygon.length;
  for (let i = 0, j = n - 1; i < n; j = i++) {
    const [latI, lngI] = polygon[i];
    const [latJ, lngJ] = polygon[j];
    const intersects =
      lngI > lng !== lngJ > lng &&
      lat < ((latJ - latI) * (lng - lngI)) / (lngJ - lngI) + latI;
    if (intersects) inside = !inside;
  }
  return inside;
}

const validateLocation = (req, res, next) => {
  const { location } = req.body;
  const { lat, lng } = location || {};

  if (!Number.isFinite(lat) || !Number.isFinite(lng)) {
    return res.status(400).json({ error: 'La ubicación es obligatoria.' });
  }

  if (!isInsidePolygon(lat, lng, VILLA_MARIA_POLYGON)) {
    return res.status(400).json({
      error: 'Solo podés reportar incidentes dentro del ejido urbano de Villa María.',
    });
  }

  next();
};

module.exports = { validateLocation };
